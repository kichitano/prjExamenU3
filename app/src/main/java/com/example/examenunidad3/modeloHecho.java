package com.example.examenunidad3;

public class modeloHecho {
    String id, titulo, descripcion, primerhecho, segundohecho;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getPrimerhecho() {
        return primerhecho;
    }

    public void setPrimerhecho(String primerhecho) {
        this.primerhecho = primerhecho;
    }

    public String getSegundohecho() {
        return segundohecho;
    }

    public void setSegundohecho(String segundohecho) {
        this.segundohecho = segundohecho;
    }
}
